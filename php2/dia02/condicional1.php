<?php
  // Condicional 1 en PHP
  // Usar la sentencia if
  // Permite tomar una decisión
  // Ejecutar o no unas instrucciones según
  //   se cumpla cierta condición


  $numero = -7;

  if ($numero > 0) // ¿El número es mayor que 0?
  {
      // Sí, el número es mayor que cero
      echo "$numero es positivo";
  }
  else // y sino
  {
      // No, el número no es mayor que cero
      echo "$numero es negativo";
  }

  // Las llaves { } delimitan un bloque de instrucciones


 ?>
